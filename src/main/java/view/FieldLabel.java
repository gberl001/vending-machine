package view;

import java.awt.Font;

import javax.swing.JLabel;
import javax.swing.SwingConstants;

@SuppressWarnings("serial")
public class FieldLabel extends JLabel {

	public final static int FONT_SIZE = 16;
	//public final static Dimension PREFERRED_SIZE = new Dimension(100, 40);
	
	public FieldLabel(String text) {
		this(text, SwingConstants.LEFT);
	}
	
	public FieldLabel(String text, int horizontalAlignment) {
		super(text, horizontalAlignment);
		this.setFont(new Font("default", 0, FONT_SIZE));
		//this.setPreferredSize(new Dimension(200, 40));
	}
	
}
